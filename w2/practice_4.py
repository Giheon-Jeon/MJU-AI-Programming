# 실습 4 — 퀴즈 점수 분석기
# 초기 점수 리스트: [88, 76, 93, 100, 69]
# 조건 1. 새 점수 84를 추가하라.
# 조건 2. 잘못 입력된 76을 제거하라.
# 조건 3. 오름차순 정렬하라.
# 조건 4. 정렬된 리스트의 첫 번째 값(최저점), 마지막 값(최고점), 평균을 출력하라.

scores = [88, 76, 93, 100, 69]

# 1. 84 추가
# 💡 힌트: 리스트의 맨 뒤에 요소를 추가하는 메서드를 사용하세요.
scores.append(84)

# 2. 76 제거
# 💡 힌트: 리스트에서 특정 값을 찾아 지우는 메서드를 사용하세요.
scores.remove(76)

# 3. 오름차순 정렬
# 💡 힌트: 리스트를 정렬하는 메서드를 사용하세요.
scores.sort()

# 4. 최저점, 최고점, 평균 계산 후 출력
# 최저점: 정렬된 리스트의 첫 번째 요소 (인덱스 사용)
# 최고점: 정렬된 리스트의 마지막 요소 (음수 인덱스 사용)
# 평균: sum() / len() 연산
lowest = scores[0]
highest = scores[-1]
average = sum(scores) / len(scores)

print(f"정렬된 점수: {scores}")
# 평균은 소수점 첫째 자리까지 표시하려면 {average:.1f} 형식으로 출력해보세요.
print(f"최저점: {lowest}, 최고점: {highest}, 평균: {average:.1f}")
