# 실습 3 — 사이트별 비밀번호 만들기
# 조건 1. http:// 부분은 제외한다.
# 조건 2. 첫 번째 점(.) 이후 부분도 제외한다.
# 조건 3. 남은 글자의 처음 세 글자 + 글자 수 + 'e'의 개수 + '!' 로 비밀번호 생성

url = "http://naver.com"

# 1. "http://" 부분 제거
# 💡 힌트: 문자열의 replace() 메서드를 사용하여 ""(빈 문자열)로 바꿔보세요.
my_str = url.replace("http://", "")

# 2. 첫 번째 점(.) 위치 찾아서 그 앞까지만 자르기
# 💡 힌트: index(".")로 점의 위치를 찾고, 슬라이싱[:위치]을 활용해보세요.
my_str = my_str[:my_str.index(".")]

# 3. 비밀번호 조합
# - 처음 세 글자: 슬라이싱 활용
# - 글자 수: len() 결과를 문자열(str)로 변환
# - 'e'의 개수: count() 결과를 문자열(str)로 변환
# - 위 내용과 마지막 "!"를 `+` 연산자로 이어붙입니다.
password = my_str[:3] + str(len(my_str)) +str(my_str.count("e")) + "!"

print(f"생성된 비밀번호: {password}")
