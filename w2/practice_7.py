# 실습 7 — 기온 추천 시스템
# 기온을 입력받은 뒤 아래 규칙에 맞는 문장을 출력하라.
# 30도 이상: '너무 더워요'
# 10도 이상 ~ 30도 미만: '야외 활동하기 좋아요'
# 0도 이상 ~ 10도 미만: '외투를 챙기세요'
# 0도 미만: '외출하지 마세요'

# 1. 현재 기온 입력받기 (정수로 변환)
temp = int(input("현재 기온 입력 >> "))

# 2. 다중 조건문(if-elif-else) 작성
# 💡 힌트: 위에서부터 차례대로(temp >= 30, temp >= 10, ...) 검사되므로, 
# elif에는 상한 범위를 다시 쓸 필요가 없습니다(if에서 이미 걸러지기 때문).

if temp >= 30:
    print("너무 더워요")
elif temp >= 10:
    print("야외 활동하기 좋아요")
elif temp >= 0:
    print("외투를 챙기세요")
else:
    print("외출하지 마세요")