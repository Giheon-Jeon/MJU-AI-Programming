# 실습 5 — 무인계산기 프로그램
# 조건 1. 성인(20세 이상)은 10,000원
# 조건 2. 미성년자(20세 미만)는 5,000원
# 조건 3. 나이를 입력받아 해당 금액을 출력하라
# 출력 형식: '결제 금액은 x원입니다.'

# 1. 나이 입력받기
# 💡 힌트: input() 결과를 int()로 감싸 숫자형(정수)으로 변환해야 합니다.
age = int(input("나이 입력 >> "))

# 2. 조건문을 사용하여 나이에 따른 가격(price) 설정
# 💡 힌트: if age >= 20: 형태로 조건을 작성하세요.
if age >= 20:
    price = 10000
else:
    price = 5000

# 3. 결과 출력
print(f"결제 금액은 {price}원입니다.")
