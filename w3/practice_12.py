# 실습 12: 유아 / 청소년 / 성인 분류
# - 0 미만: "나이는 0 이상이어야 합니다."
# - 0~7: 유아
# - 8~13: 청소년 (초등학생)
# - 14~16: 청소년 (중학생)
# - 17~19: 청소년 (고등학생)
# - 20 이상: 성인
# - 팁: 큰 그룹(유아/청소년/성인)을 먼저 나누고, 청소년일 때만 중첩 if문으로 내부 분류를 해보세요.

age = int(input("나이를 입력하세요: "))

if age < 0:
    print("나이는 0 이상이어야 합니다.")
    exit()

if age <= 7:
    print("유아")
elif age <= 19:
    if age <= 13:
        print("청소년(초등학생)")
    elif age <= 16:
        print("청소년(중학생)")
    else:
        print("청소년(고등학생)")
else:
    print("성인")